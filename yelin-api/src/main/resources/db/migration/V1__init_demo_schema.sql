create table app_user (
    id bigint generated by default as identity primary key,
    full_name varchar(255) not null,
    email varchar(255) not null unique,
    password_hash varchar(255) not null,
    role varchar(50) not null,
    position_title varchar(255) not null,
    active boolean not null default true,
    created_at timestamp not null default current_timestamp
);

create table project (
    id bigint generated by default as identity primary key,
    name varchar(255) not null,
    description varchar(1000),
    customer varchar(255) not null,
    address varchar(500) not null,
    object_type varchar(100) not null,
    manager_user_id bigint not null references app_user(id),
    start_date date not null,
    planned_finish_date date not null,
    actual_finish_date date,
    current_stage_name varchar(255),
    status varchar(50) not null,
    completion_percent integer not null default 0,
    created_at timestamp not null default current_timestamp,
    updated_at timestamp not null default current_timestamp
);

create table project_member (
    id bigint generated by default as identity primary key,
    project_id bigint not null references project(id) on delete cascade,
    user_id bigint not null references app_user(id) on delete cascade,
    project_role varchar(100) not null
);

create table project_stage (
    id bigint generated by default as identity primary key,
    project_id bigint not null references project(id) on delete cascade,
    name varchar(255) not null,
    description varchar(1000),
    sort_order integer not null,
    responsible_user_id bigint references app_user(id),
    start_date date,
    deadline date not null,
    finish_date date,
    status varchar(50) not null,
    completion_percent integer not null default 0
);

create table document_record (
    id bigint generated by default as identity primary key,
    project_id bigint not null references project(id) on delete cascade,
    stage_id bigint not null references project_stage(id) on delete cascade,
    name varchar(255) not null,
    document_type varchar(100) not null,
    current_version integer not null default 1,
    file_name varchar(255) not null,
    file_extension varchar(20) not null,
    file_size bigint not null default 0,
    uploaded_by_user_id bigint not null references app_user(id),
    status varchar(50) not null,
    uploaded_at timestamp not null default current_timestamp,
    comment_text varchar(1000)
);

create table document_version (
    id bigint generated by default as identity primary key,
    document_id bigint not null references document_record(id) on delete cascade,
    version_number integer not null,
    file_name varchar(255) not null,
    uploaded_by_user_id bigint not null references app_user(id),
    uploaded_at timestamp not null default current_timestamp,
    version_comment varchar(1000)
);

create table remark (
    id bigint generated by default as identity primary key,
    project_id bigint not null references project(id) on delete cascade,
    stage_id bigint not null references project_stage(id) on delete cascade,
    document_id bigint not null references document_record(id) on delete cascade,
    text_value varchar(1000) not null,
    author_user_id bigint not null references app_user(id),
    assignee_user_id bigint references app_user(id),
    priority varchar(50) not null,
    status varchar(50) not null,
    fix_deadline date,
    created_at timestamp not null default current_timestamp,
    closed_at timestamp,
    executor_comment varchar(1000)
);

create table notification_record (
    id bigint generated by default as identity primary key,
    user_id bigint not null references app_user(id) on delete cascade,
    message_text varchar(1000) not null,
    object_link varchar(500),
    is_read boolean not null default false,
    created_at timestamp not null default current_timestamp
);

create table audit_log_entry (
    id bigint generated by default as identity primary key,
    user_id bigint references app_user(id) on delete set null,
    action_type varchar(100) not null,
    object_name varchar(255) not null,
    description_text varchar(1000) not null,
    created_at timestamp not null default current_timestamp
);

